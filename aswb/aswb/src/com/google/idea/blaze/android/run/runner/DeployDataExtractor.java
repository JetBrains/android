package com.google.idea.blaze.android.run.runner;

import static com.google.common.collect.ImmutableList.toImmutableList;

import com.android.tools.idea.run.ApkProvisionException;
import com.google.common.collect.ImmutableList;
import com.google.common.collect.MoreCollectors;
import com.google.devtools.build.lib.rules.android.deployinfo.AndroidDeployInfoOuterClass.AndroidDeployInfo;
import com.google.devtools.build.lib.rules.android.deployinfo.AndroidDeployInfoOuterClass.Artifact;
import com.google.idea.blaze.android.manifest.ManifestParser;
import com.google.idea.blaze.android.manifest.ManifestParser.ParsedManifest;
import com.google.idea.blaze.android.run.deployinfo.DeployData;
import com.google.idea.blaze.base.qsync.cache.ArtifactFetchers;
import com.google.idea.blaze.base.scope.BlazeContext;
import com.google.idea.blaze.base.scope.output.StatusOutput;
import com.google.idea.blaze.common.Label;
import com.google.idea.blaze.common.artifact.OutputArtifact;
import com.intellij.openapi.project.Project;
import java.io.BufferedInputStream;
import java.io.IOException;
import java.io.InputStream;
import java.util.Collection;
import java.util.List;

/** Utilities to create {@link DeployData} for specific targets. */
final class DeployDataExtractor {
  private DeployDataExtractor() {}

  public static DeployData extract(
    Label label,
      Collection<OutputArtifact> infoBuildArtifacts,
      Collection<OutputArtifact> apkBuildArtifacts,
      String deployInfoSuffix,
      BlazeContext context,
      Project project)
    throws ApkProvisionException {
    // AndroidDeployInfo is a proto that is generated by Blaze and gives us the APKs to install
    // and the merged manifest file.
    AndroidDeployInfo deployInfoProto;
    try {
      context.output(new StatusOutput("Parsing deployment information."));
      deployInfoProto = fetchAndParseDeployInfo(infoBuildArtifacts, deployInfoSuffix, context, project);
    } catch (IOException e) {
      throw new ApkProvisionException("Error getting deployinfo: " + e.getMessage(), e);
    }

    List<String> apkPaths =
        deployInfoProto.getApksToDeployList().stream()
            .map(Artifact::getExecRootPath)
            .map(DeployDataExtractor::getBazelOutRelativePath)
            .collect(toImmutableList());
    ImmutableList<OutputArtifact> apkArtifacts =
        apkBuildArtifacts.stream()
            .filter(a -> apkPaths.contains(getBazelOutRelativePath(a.getArtifactPath().toString())))
            .collect(toImmutableList());

    String mergedManifestPath = getBazelOutRelativePath(deployInfoProto.getMergedManifest().getExecRootPath());
    OutputArtifact manifestArtifact =
        infoBuildArtifacts.stream()
            .filter(a -> mergedManifestPath.equals(getBazelOutRelativePath(a.getArtifactPath().toString())))
            .collect(MoreCollectors.onlyElement());

    try {
      return DeployData.create(label, parseManifest(manifestArtifact, context, project), apkArtifacts);
    }
    catch (IOException e) {
      throw new ApkProvisionException("Cannot parse manifest artifact", e);
    }
  }

  private static String getBazelOutRelativePath(String str) {
    if (str.startsWith("blaze-out/") || str.startsWith("bazel-out/")) {
      return str.substring("blaze-out/".length());
    }
    return str;
  }

  private static AndroidDeployInfo fetchAndParseDeployInfo(
      Collection<OutputArtifact> bepOutput, String deployInfoSuffix, BlazeContext context, Project project) throws IOException {
    OutputArtifact deployInfoArtifact =
        bepOutput.stream()
            .filter(a -> a.getBazelOutRelativePath().endsWith(deployInfoSuffix))
            .collect(MoreCollectors.onlyElement());
    try (BufferedInputStream inputStream = ArtifactFetchers.downloadArtifact(deployInfoArtifact, context, project)) {
      return AndroidDeployInfo.parseFrom(inputStream);
    }
  }

  private static ParsedManifest parseManifest(OutputArtifact mergedManifestArtifact, BlazeContext context, Project project)
      throws IOException {
    try (InputStream is = ArtifactFetchers.downloadArtifact(mergedManifestArtifact, context, project)) {
      return ManifestParser.parseManifestFromInputStream(is);
    }
  }
}
